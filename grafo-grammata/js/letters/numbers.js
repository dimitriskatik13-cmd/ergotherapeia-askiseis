const shift = (pts, dx) => pts.map((p) => ({ x: p.x + dx, y: p.y }));

function makeNumber(n) {
  const str = String(n);
  let parts;
  if (str.length === 1) {
    parts = DIGITS[+str].map((pts) => shift(pts, 0.28));
  } else {
    parts = [
      ...DIGITS[+str[0]].map((pts) => shift(pts, 0.06)),
      ...DIGITS[+str[1]].map((pts) => shift(pts, 0.50)),
    ];
  }
  return {
    char: str,
    ph: `num-${n}`,          // sounds/num-<n>.mp3 → το ΟΝΟΜΑ του αριθμού (π.χ. «δεκαεπτά»)
    keyword: null,
    strokes: parts.map((pts, i) => S(i + 1, pts)),
  };
}

export const NUMBERS = Array.from({ length: 32 }, (_, n) => makeNumber(n));
